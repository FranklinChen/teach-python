"""Review lists and dictionaries."""


def number_of_items_in_list(the_list):
    """Return the number of items in the_list"""

    return len(the_list)


def number_of_rooms_in_house(house):
    """house is a dictionary that has a key 'rooms' where the value is
    a list of rooms. Return the number of rooms in the house.
    """

    return len(house['rooms'])
